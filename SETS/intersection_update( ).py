# set1.intersection_update(set2) :- this function will keep only the items that are present in both sets.

x={1,2,3}
y={3,4,5}
x.intersection_update(y)
print(x)
print()

# set1.intersection(set2) :- this function will return a new set that only contains the items that are present in both sets,
x={1,2,3}
y={3,4,5}
print(x.intersection(y))
print(x)
