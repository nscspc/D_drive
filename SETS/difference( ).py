# set1.difference(set2) :- by this function we can obtain only those elements of set1 which are not present in set 2 .
'''
The difference of A and B (A - B) is the set with all
elements that arein A but not in B. Consequently,
(B - A) is the set with all the elements in B but not in A.
'''
s1={1,2,3,4,5}
s2={4,5,6,4,5}
print(s1.difference(s2))
