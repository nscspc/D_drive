# ceil function :- ceil function returns a value which is greater and most nearest integer compared to the argumented float value. #
# floor function :- floor function returns a value which is smaller and most nearest integer compared to the argumented float value #
# these functions are defined in math library(module) #
import math
print(math.ceil(6.7),end="  ")
print(math.ceil(6.9),end="  ")
print(math.ceil(6),end="  ")
print(math.ceil(6.1))

print(math.floor(6.7),end="  ")
print(math.floor(6.9),end="  ")
print(math.floor(6),end="  ")
print(math.floor(6.1))
